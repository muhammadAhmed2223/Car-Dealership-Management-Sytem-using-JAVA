package Controller;

import java.io.IOException;

import Model.AddRepairItem;
import application.Main;
import javafx.fxml.FXML;
import javafx.scene.control.Label;
import javafx.scene.control.MenuBar;
import javafx.scene.control.TextField;

import javafx.scene.input.MouseEvent;
import javafx.stage.Stage;

public class RepairItemController {
	Main m = new Main();
	@FXML
	MenuBar menuBar;
	@FXML
	private Label label1;
	@FXML
	private TextField textbox1;// repair option
	@FXML
	private TextField textbox2;// vehicle type
	@FXML
	private TextField textbox3;// cost

	// Event Listener on Button.onMouseClicked
	@FXML
	public void repair1(MouseEvent event) {
		// TODO Autogenerated
		System.out.println(textbox1.getText());
		System.out.println(textbox2.getText());
		System.out.println(textbox3.getText());
		
		
		String repairOpt = textbox1.getText();
		String vehicleType= textbox2.getText();
		int cost =Integer.parseInt(textbox3.getText());
		
		AddRepairItem addRepairItem = new AddRepairItem();
		
		int result = addRepairItem.addNewRepairItem(repairOpt, vehicleType, cost);
		if (result == 0) {
			System.out.println("Successfully Added Repair Item");
		}else {
			label1.setText("Error");
		}
	}
	
	public void goBack() throws IOException {
		Stage s = (Stage) menuBar.getScene().getWindow();
		m.changeScene(s, "../View/Dealer.fxml");
	}
}

